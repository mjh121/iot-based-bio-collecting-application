    private void setStatus(int collectorType, String deviceId, int btnType){
        
        Collector collector = null;
        String msg = "";
        Color color = null;
        if(btnType == Constants.ON){
            msg = "Running";
            color = new Color(0, 255, 0);
        }else if(btnType == Constants.OFF){
            msg = "Stopped";
            color = new Color(255, 0, 0);
        }
        
        switch(collectorType){
            case Constants.VISION:
                status_vision.setText(msg);
                status_vision.setForeground(color);
                jLabel_CAM1_status.setText(msg);
                jLabel_CAM1_status.setForeground(color);
                dm.setVisionDeviceStatus(btnType); break;
            case Constants.BIO:
                status_bio.setText(msg);
                status_bio.setForeground(color);
                if(deviceId == Constants.BIO_DEVICE_NAME_1) {
                    jLabel_gear1_status.setText(msg);
                    jLabel_gear1_status.setForeground(color);
                } else if(deviceId == Constants.BIO_DEVICE_NAME_2) {
                    jLabel_gear2_status.setText(msg);
                    jLabel_gear2_status.setForeground(color);
                } else if(deviceId == Constants.BIO_DEVICE_NAME_3) {
                    jLabel_gear3_status.setText(msg);
                    jLabel_gear3_status.setForeground(color);
                }
                
                dm.setBioDeviceStatus(deviceId, btnType); break;
            case Constants.VOICE:
                status_voice.setText(msg);
                status_voice.setForeground(color);
                jLabel_MIC1_status.setText(msg);
                jLabel_MIC1_status.setForeground(color);                
                dm.setVoiceDeviceStatus(btnType); break;            
           default:
        }
    }
    
    private void pushOnOff(int collectorType, int btnType) {
        Collector collector = null;
        String deviceId = "";
        
        switch(collectorType){
            case Constants.VISION: 
                deviceId = deviceId_vision.getText();
                collector = new CollectorVision(); break;
            case Constants.BIO: 
                deviceId = deviceId_bio.getText();
                collector = new CollectorBio(); break;
            case Constants.VOICE: 
                deviceId = deviceId_voice.getText();
                collector = new CollectorVoice(); break;              
           default:
        }
        
        int res = Constants.INIT_RESULT;
        
        switch(btnType){
            case Constants.ON :  res= collector.startCollecting(); break;
            case Constants.OFF :  res= collector.stopCollecting(); break;
        }
               
        if( res == Constants.DONE){
            setStatus(collectorType, deviceId, btnType);
        }else{
            //error!
        }       
    }
            
