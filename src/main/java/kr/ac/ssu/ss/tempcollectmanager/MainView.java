    private void jToggleButton_CAM1_EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton_CAM1_EditActionPerformed
        if(jToggleButton_CAM1_Edit.isSelected()) {
            tmpUserID = jTextField_CAM1_userID.getText();
            jTextField_CAM1_userID.setEditable(true);
            jTextField_CAM1_userID.setBackground(Color.pink);
        } else {
            jTextField_CAM1_userID.setEditable(false);
            jTextField_CAM1_userID.setBackground(Color.white);
            String inputUserID = jTextField_CAM1_userID.getText();
            
            if(um.containId(inputUserID)) {
                dm.setVisionDeviceUserID(inputUserID);
                if(!tmpUserID.equals("none")) {
                    um.getUser(tmpUserID).setVision_d_id("none");
                }
                um.getUser(inputUserID).setVision_d_id(Constants.VISION_DEVICE_NAME);                
            } else {
                jTextField_CAM1_userID.setText(tmpUserID);
            }
        }
    }//GEN-LAST:event_jToggleButton_CAM1_EditActionPerformed

