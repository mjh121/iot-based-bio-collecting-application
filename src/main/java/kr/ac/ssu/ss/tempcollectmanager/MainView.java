    private void initDeviceList() {
        jTextField_CAM1_userID.setText(dm.getVisionDeviceUserID());
        jTextField_MIC1_userID.setText(dm.getVoiceDeviceUserID());
        jTextField_Gear1_userID.setText(dm.getBioDeviceUserID(Constants.BIO_DEVICE_NAME_1));
        jTextField_Gear2_userID.setText(dm.getBioDeviceUserID(Constants.BIO_DEVICE_NAME_2));
        jTextField_Gear3_userID.setText(dm.getBioDeviceUserID(Constants.BIO_DEVICE_NAME_3));
        
        // Device and User Mapping info setting
        if(um.containId(dm.getVisionDeviceUserID())) {
            um.getUser(dm.getVoiceDeviceUserID()).setVision_d_id(Constants.VISION_DEVICE_NAME); 
        } if(um.containId(dm.getVoiceDeviceUserID())) {
            um.getUser(dm.getVoiceDeviceUserID()).setVoice_d_id(Constants.VOICE_DEVICE_NAME); 
        } if(um.containId(dm.getBioDeviceUserID(Constants.BIO_DEVICE_NAME_1))) {
            um.getUser(dm.getBioDeviceUserID(Constants.BIO_DEVICE_NAME_1)).setBio_d_id(Constants.BIO_DEVICE_NAME_1); 
        } if(um.containId(dm.getBioDeviceUserID(Constants.BIO_DEVICE_NAME_2))) {
            um.getUser(dm.getBioDeviceUserID(Constants.BIO_DEVICE_NAME_2)).setBio_d_id(Constants.BIO_DEVICE_NAME_2); 
        } if(um.containId(dm.getBioDeviceUserID(Constants.BIO_DEVICE_NAME_3))) {
            um.getUser(dm.getBioDeviceUserID(Constants.BIO_DEVICE_NAME_3)).setBio_d_id(Constants.BIO_DEVICE_NAME_3); 
        }
    }
